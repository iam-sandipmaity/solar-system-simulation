import { TEXTURE_URLS } from './textureUrls';

export interface MoonData {
  name: string;
  radius: number;              // km
  orbitRadius: number;         // km from planet center
  orbitalPeriod: number;       // days (negative = retrograde)
  orbitalInclination?: number; // degrees
  gravity?: number;            // m/s²
  mass?: string;               // human-readable, e.g. '7.342 × 10²² kg'
  surfaceTemps?: { min?: number; max?: number; mean?: number }; // °C
  textureKey?: string;         // optional — resolves to /textures/2k_{key}.jpg
  color: string;               // tint / fallback color
}

export interface PlanetData {
  id: string;
  name: string;
  type: 'terrestrial' | 'gas-giant' | 'ice-giant' | 'dwarf' | 'star';
  radius: number;          // km
  mass: number;            // kg
  semiMajorAxis: number;   // km
  eccentricity: number;
  inclination: number;     // degrees
  longitudeOfAscendingNode: number; // degrees (Ω)
  argumentOfPerihelion: number;     // degrees (ω)
  orbitalPeriod: number;   // days
  rotationPeriod: number;  // days (negative = retrograde)
  axialTilt: number;       // degrees
  surfaceGravity: number;  // m/s²
  escapeVelocity: number;  // km/s
  meanTemperature: number; // °C
  atmosphericPressure: number | null; // bar
  atmosphericComposition: string;
  moons: number;
  rings: boolean;
  satelliteData?: MoonData[];
  color: string;           // fallback hex color
  emissive?: boolean;
  textures: Partial<Record<'diffuse' | 'bump' | 'specular' | 'clouds' | 'night' | 'ring', string>>;
  funFacts: string[];
}

